/*
 
  3) Question:- Loops(For, While, Do-While).
  
  Loops are used to execute a block of code repeatedly as long as a specified condition is true. 
  Java provides three main types of loops: for, while, and do-while.
  
 1. for Loop
 The for loop is used when the number of iterations is known beforehand.
 It consists of three parts: initialization, condition, and increment/decrement.
 
 
 2. while Loop
 The while loop is used when the number of iterations is not known in advance, and it depends on a condition being true.
 The condition is checked before the code is executed.
  
  
3. do-while Loop
The do-while loop is similar to the while loop, except the condition is checked after the code is executed.
This guarantees that the code runs at least once.
 
 */


