/*
  1) Questions :- Constructor Types (Default, Parameterized).
  
1. Default Constructor
A default constructor is a constructor that is provided by the Java compiler automatically if no constructor is explicitly defined in the class. 
It does not take any arguments and simply initializes the object with default values.

2. Parameterized Constructor
A parameterized constructor is a constructor that takes arguments to initialize an object with specific values. 
You can define one or more parameterized constructors in a class. 
This allows you to provide initial values for the fields when the object is created.
  
  
 */


