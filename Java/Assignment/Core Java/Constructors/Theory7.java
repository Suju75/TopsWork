/*
   3) Questions :- Constructor Overloading .
   
   Constructor overloading in Java refers to defining multiple constructors in a class with different parameter lists.
   This allows the creation of objects in different ways, depending on the arguments passed during object instantiation.

   How Constructor Overloading Works.
   
   1)Multiple constructors are defined with the same name (the class name) but with different parameter types or numbers.
   2)The Java compiler differentiates the constructors based on their parameter lists (number, type, and order of parameters).
   3)Depending on the arguments provided during object creation, the appropriate constructor is invoked.
   
 */


