/*

  1) Questions :- Inheritance Types and Benefit.
  
  Inheritance in Java
  
  Inheritance is a core concept in object-oriented programming (OOP) that allows a class (child class) to inherit
  the properties and behaviors (fields and methods) of another class (parent class). It enables code re usability 
  and establishes a hierarchical relationship between classes.
  
  Types of Inheritance in Java
  
 1) Single Inheritance
    A child class inherits from a single parent class.
    
 2) Multilevel Inheritance
    A class inherits from another class, and a third class inherits from the second class.

 3)Hierarchical Inheritance
   Multiple child classes inherit from a single parent class.
   
 4) Hybrid Inheritance (via Interfaces)
  Combines two or more types of inheritance. Since Java does not support multiple inheritance with classes 
  to avoid ambiguity, it uses interfaces for hybrid inheritance.
  
 
  
*/

