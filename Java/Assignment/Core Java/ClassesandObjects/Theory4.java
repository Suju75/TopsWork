/*

3)Questions:-  Object Creation, Accessing Members of the Class.

Object Creation
 In Java, objects are instances of a class. 
 Objects are created using the new keyword, which allocates memory for the object and initializes its fields.
 
 Syntax for Creating an Object:
 
 ClassName objectName = new ClassName();
 
 
 Accessing Members of a Class:

    To access the fields and methods of a class, use the dot operator (.) with the object name.
    
    Syntax
    objectName.fieldName;  // Accessing fields
    objectName.methodName(); // Accessing methods
 
 
*/

