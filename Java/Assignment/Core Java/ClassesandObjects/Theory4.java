/*

2)Questions:- Constructors and Overloading.

Constructors in Java
A constructor is a special method used to initialize objects. It is called automatically when an object is created.

Characteristics of a Constructor:
  
1)The name of the constructor must be the same as the class name.
2)It does not have a return type (not even void).
3)It is automatically invoked when an object is created.
  
Constructor Overloading:
  
Constructor overloading is when a class has multiple constructors with different parameter lists.
The constructor that matches the arguments passed during object creation is called.
 
   
Types of Constructors:

  1)Default Constructor:
  1)A constructor with no parameters.
  2)It initializes the object with default values.
  
  
  2)Parameterized Constructor:
  1)A constructor that takes arguments to initialize the object with specific values.
  
  3)Copy Constructor:
  Copy constructor (object as parameter) clone the object.
  
  
*/


